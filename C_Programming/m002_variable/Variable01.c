#include <stdio.h>

int main() {

    int a; // 'a' is data type Integer
    a = 100;

    float b; // 'b' is data type Float
    b = 3.14;

    double c; // 'c' is data type Double
    c = 10.123456789;

    char d; // 'd' is data type Character
    d = 'K';

    printf("a = %d \n", a); // a = 100 (int)
    printf("b = %f \n", b); // b = 3.14 (float)
    printf("c = %f \n", c); // c = 10.123456789 (double)
    printf("d = %c \n", d); // d = 'K'' (char)

    return 0;
}
