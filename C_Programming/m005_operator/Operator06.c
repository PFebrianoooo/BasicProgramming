#include <stdio.h>

int main() {

    int a = 10, b = 5;

    int result1 = a > 5;  // result1 = true
    int result2 = b > 10; // result2 = false

    // Logical NOT ( ! )
    printf("!result1  = %d \n", !result1);
    printf("!result2  = %d \n", !result2);

    return 0;
}
