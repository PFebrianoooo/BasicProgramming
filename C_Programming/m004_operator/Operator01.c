#include <stdio.h>

int main() {

    // Operator Arithmetic
    int a = 10, b = 4;
    printf("%d + %d = %d \n", a, b, a + b);
    printf("%d - %d = %d \n", a, b, a - b);
    printf("%d * %d = %d \n", a, b, a * b);
    printf("%d / %d = %d \n", a, b, a / b);
    printf("%d mod %d = %d \n", a, b, (a % b));

    return 0;
}
