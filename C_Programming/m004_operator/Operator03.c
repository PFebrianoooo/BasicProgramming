#include <stdio.h>

int main() {

    // Relational Operators
    int a = 10, b = 5, c = 10, d = 20;

    printf("a = %d, b = %d, c = %d, d = %d \n", a, b, c, d);
    printf("a == b --> %d \n", a == b);
    printf("a == c --> %d \n", a == c);
    printf("a != b --> %d \n", a != b);
    printf("a != c --> %d \n", a != c);
    printf("a > b --> %d \n", a > b);
    printf("a > d --> %d \n", a > d);
    printf("a >= c --> %d \n", a >= c);
    printf("b < a --> %d \n", b < a);
    printf("d < a --> %d \n", d < a);
    printf("c <= a --> %d \n", c <= a);

    return 0;
}
