

import Cocoa

// UInt ( unsigned integer )
// UInt32, UInt64

// minValue sama dengan 0, dan bertipe UInt32
let minValueUInt32 = UInt32.min

// maxValue sama dengan 4294967295, dan bertipe UInt32
let maxValueUInt32 = UInt32.max

// minValue sama dengan 0, dan bertipe UInt64
let minValueUInt64 = UInt64.min

// maxValue sama dengan 9223372036854775807, dan bertipe UInt64
let maxValueUInt64 = UInt64.max

print("UInt32: ")
print("Minimum UInt32: \(minValueUInt32)")
print("Maximum UInt32: \(maxValueUInt32)")
print("")
print("UInt64: ")
print("Minimum UInt64: \(minValueUInt64)")
print("Maximum UInt64: \(maxValueUInt64)")
