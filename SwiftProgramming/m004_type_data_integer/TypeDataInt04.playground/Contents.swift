

import Cocoa

// Int32, Int64

// minValue sama dengan -2147483648, dan bertipe Int32
let minValueInt32 = Int32.min

// maxValue is equal to 2147483647, dan bertipe Int32
let maxValueInt32 = Int32.max

// minValue sama dengan -9223372036854775808, dan bertipe Int64
let minValueInt64 = Int64.min

// maxValue sama dengan 9223372036854775807, dan bertipe Int64
let maxValueInt64 = Int64.max

print("Int32: ")
print("Minimum Int32: \(minValueInt32)")
print("Maximum Int32: \(maxValueInt32)")
print("")
print("Int64: ")
print("Minimum Int64: \(minValueInt64)")
print("Maximum Int64: \(maxValueInt64)")


