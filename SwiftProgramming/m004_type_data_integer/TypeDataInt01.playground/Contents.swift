
import Cocoa

// UInt ( unsigned integer )
// UInt8, UInt16

// minValue is equal to 0, and is of type UInt8
let minValueUInt8 = UInt8.min

// maxValue is equal to 255, and is of type UInt8
let maxValueUInt8 = UInt8.max

// minValue sama dengan 0, dan bertipe UInt16
let minValueUInt16 = UInt16.min

// maxValue sama dengan 65535, dan bertipe UInt16
let maxValueUInt16 = UInt16.max
 
print("UInit8: ")
print("Minimum UInt8 = \(minValueUInt8)")
print("Maximum UInt8 = \(maxValueUInt8)")
print("")
print("UInit16: ")
print("Minimum UInt16 = \(minValueUInt16)")
print("Maximum UInt16 = \(maxValueUInt16)")



