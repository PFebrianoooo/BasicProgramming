

import Cocoa

// AND Operator
var and1 = true && true; print(and1)
var and2 = true && false; print(and2)
var and3 = false && true; print(and3)
var and4 = false && false; print(and4)

// OR Operator
var or1 = true || true; print(or1)
var or2 = true || false; print(or2)
var or3 = false || true; print(or3)
var or4 = false || false; print(or4)

// NOT Operator
var not1 = !true; print(not1)
var not2 = !false; print(not2)
