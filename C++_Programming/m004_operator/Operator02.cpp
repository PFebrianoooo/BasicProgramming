#include <iostream>
using namespace std;

int main() {
    // Arithmetic Operator ( Increment, Decrement )
    int x = 4, y = 10;

    // x++ and y--
    cout << "Before: x = " << x << " y = " << y << endl;
    cout << "After : x = " << x++ << " y = " << y-- << endl;

    // ++x and --y
    cout << "Before: x = " << x << " y = " << y << endl;
    cout << "After : x = " << ++x << " y = " << --y << endl;

    return 0;
}
